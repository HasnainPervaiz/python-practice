num = int(input("Enter A Number Between 1 And 7 To Check It's Corresponding Day: "))
if(num == 1):
    print("Monday.")
elif(num == 2):
    print("Tuesday.")
elif(num == 3):
    print("Wednesday.")
elif(num == 4):
    print("Thursday.")
elif(num == 5):
    print("Friday.")
elif(num == 6):
    print("Saturday.")
elif(num == 7):
    print("Sunday.")
else:
    print("There Is No Corresponding Day To This Number In The Week.")